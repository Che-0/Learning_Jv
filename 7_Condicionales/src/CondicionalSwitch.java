public class CondicionalSwitch {
    public static void main(String[] args) {
        int opcion = 1;
        
        switch(opcion)
        {
            case 1:
                System.out.println("Escogiste 1 ");
                break;
            
            case 2:
                System.out.println("Escogiste 2 ");
                break;
            
            default:
                System.out.println("opcion incorrecta ");
                break;
        }
    }
}
